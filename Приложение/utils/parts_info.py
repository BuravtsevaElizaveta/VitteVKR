def get_part_info(class_name: str) -> dict:
    """
    Возвращает словарь {'name': ..., 'description': ...}.
    Если класс неизвестен — возвращает name=class_name.
    """
    return PART_DETAILS.get(
        class_name,
        {"name": class_name, "description": "Описание недоступно."}
    )
